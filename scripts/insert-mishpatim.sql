## THE MAP

### Core Message (One Sentence)
**Every mitzvah has layers - the Torah doesn''t just tell you what NOT to do, it shows you who you can BECOME.**

### The Journey
1. Hook: What do you serve? (Everyone serves something)
2. Framework: The Torah''s structure - 3 "don''ts" paired with 3 "do''s"
3. Deep Dive: Each pair shows the ELEVATION, not just the prohibition
4. Takeaway: Mitzvos aren''t restrictions - they''re a ladder for transformation

### Why Should They Care?
- **If they get this:** Mitzvos stop being boxes to check and become a path to becoming their best self
- **If they don''t:** Judaism stays flat, mechanical, a list of rules with no soul

