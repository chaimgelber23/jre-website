## WHAT THEY MIGHT ASK (Be Ready)

### The Big Ones (Almost Guaranteed)

**"Isn''t slavery wrong? How can the Torah permit it?"**
- "Great question. Let me be clear - Torah ''slavery'' is nothing like what we think of when we hear that word. There''s no whips, no chains, no dehumanization."
- It''s closer to court-ordered community service with extreme worker protections
- The Gemara says: "Whoever acquires a Hebrew slave acquires a master for himself" - the MASTER has more obligations than the servant
- Same bed, same food, same quality of life - if you only have one pillow, HE gets it
- Maximum 6 years, and when he leaves he gets GIFTS
- It''s a rehabilitation system: a thief learns honest work, gets back on his feet, and leaves with resources
- "The Torah didn''t invent slavery - it existed everywhere. What the Torah did was transform it into something that protects the worker and rebuilds his life."

**"Why can a father sell his daughter? That''s terrible."**
- "I hear you - it sounds awful to modern ears. But listen to what actually happens:"
- He can ONLY do this in extreme poverty when he literally cannot feed her
- The ''buyer'' is OBLIGATED to either marry her himself or give her to his son - with full wife status, not servant status
- She can be redeemed (bought back) by her family at any time
- She goes free automatically when she reaches maturity
- "The Torah is saying: in a world where a poor girl with no family support becomes vulnerable to exploitation, here''s a system that GUARANTEES she ends up married, supported, and treated with dignity. It''s not selling - it''s arranged protection with legal obligations."

**"This seems harsh - piercing someone''s ear?"**
- It''s a small piercing, not a mutilation - think earring, not branding
- The ear specifically because it heard at Sinai "you are My servants"
- "The Torah is saying: you heard freedom declared, and you chose chains? Let your ear carry that reminder."
- It''s actually merciful - he GETS what he asked for (to stay), but with a reminder
- "Better a pierced ear than a surrendered soul."

### Philosophical Pushback

**"Who cares about ''levels''? I just want to be a good person."**
- "That''s exactly what levels means. Being a ''good person'' looks different at different stages of growth."
- Not murdering someone is level 1. Treating a criminal with dignity is level 10. Both are being a ''good person'' - just at different heights
- "The Torah isn''t saying you''re bad if you''re at the floor. It''s showing you there''s a ceiling you didn''t know existed."
- Think of it like fitness - you don''t shame someone for jogging. But you also don''t pretend that jogging and running a marathon are the same thing.

**"Why does God need to command love? Shouldn''t it be natural?"**
- "Great question. Love IS natural - but only for people we like. The Torah is talking about loving people who are HARD to love."
- A thief. A criminal. Someone who has fallen.
- Natural love is easy. Commanded love is what separates growth from instinct.
- "It''s like saying ''shouldn''t honesty be natural?'' Sure - until it costs you something. That''s when you need the commandment."

**"I don''t believe in God / I''m not religious. Why should I care about mitzvos?"**
- "Fair. Forget the religious framing for a second. Look at what the Torah is actually teaching:"
- Treat even criminals with dignity (Eved Ivri)
- Don''t surrender your autonomy to anything that doesn''t deserve it (Nirtzah)
- Protect vulnerable people proactively (Amah Ivriah)
- "These are universal principles. The Torah just organized them 3,000 years before anyone else."
- "You don''t have to believe in God to see the wisdom in a system that says ''here''s the minimum, and here''s the ideal - keep climbing.''"

**"Doesn''t the ''floor to ceiling'' idea create guilt? Like you''re never good enough?"**
- Opposite. It removes guilt because wherever you are IS your starting point
- "The Torah doesn''t say ''you should already be at the ceiling.'' It says ''here''s your next step.''"
- The question isn''t "why aren''t you at the top?" It''s "are you climbing?"
- Reb Zusha''s question: "Why weren''t you Zusha?" - not "Why weren''t you Moses?"

### Halachic/Technical Questions

**"Do these laws apply today?"**
- The specific laws of Eved Ivri don''t apply when there''s no Sanhedrin and no Yovel
- But the PRINCIPLES are eternal: dignity, autonomy, protection of the vulnerable
- "We study these to understand God''s values, not just for practical application. The values apply in every generation."

**"How do the three lo saseis at the end of Yisro correspond to the three cardinal sins? That seems like a stretch."**
- Metal on the altar → Rashi explicitly connects metal to weapons and bloodshed → Murder
- No steps on the altar → Rashi says it''s about not exposing yourself → Modesty/Arayos
- No idol images → Directly Avoda Zara
- "These aren''t my connections - this is Rashi and the Mefarshim. And the parallel with this week''s parsha is Rav Wolbe."

**"What''s the source for the master treating the servant as an equal?"**
- Gemara Kiddushin 20a: "Whoever acquires a Hebrew slave has acquired a master for himself"
- Detailed in Rambam Hilchos Avadim 1:9
- Same bed, food, clothing - if master only has one pillow, the servant gets it

**"Why is the Nirtzah only punished at year 6 and not from the start?"**
- Chochmas Manoach: During the first 6 years, he retains the OPTION to buy his freedom
- Having an option to leave = not fully enslaved
- The Nirtzah FORFEITS that option - he willingly gives up his autonomy entirely
- That''s what gets punished - not service, but surrender

### Personal/Emotional Questions

**"What if I''m stuck at the floor and can''t seem to grow?"**
- "The floor isn''t a failure - it''s a foundation. You can''t build a building without a floor."
- Growth isn''t linear. Sometimes staying at the floor during a hard time IS growth
- "The question is: do you know the ceiling exists? Are you oriented toward it? That''s enough."
- Shoemaker''s candle: "As long as the candle is burning, it''s still possible to repair."

**"How do you treat someone with dignity when they''ve hurt you?"**
- "That''s the hardest version of this mitzvah. And honestly? Sometimes you can''t. And that''s OK."
- The Torah sets the IDEAL. Real life is messy. You aim for the ceiling, knowing you''ll land somewhere in the middle.
- "But here''s the thing - when you DO manage to treat someone with dignity even when they don''t deserve it, that''s the moment you grow the most."

**"What''s YOUR master? You asked us - so what''s yours?"**
- Be ready to answer this honestly. This is a growth-edge moment.
- Possible honest answers: approval, productivity, comfort, security
- "I ask myself this question too. And the honest answer is [your real answer]. That''s why this parsha hits me."

