## THE SCRIPT

### OPENING (7 minutes)

**[Start with a question - don''t open with Torah yet]**

"Let me ask you something. If someone followed you around for a week - watched how you spend your time, your energy, your money - what would they say you serve? What''s your master?

*[Pause - let them think]*

We all serve something. Some people serve their careers. Some serve their image. Some serve comfort. The question isn''t WHETHER you serve - it''s WHAT you serve.

Here''s what I want to show you tonight: The Torah anticipated this. Three thousand years ago, at Mount Sinai, God gave us a roadmap - not just for what to avoid, but for who we can become. And it starts with the most unlikely place: laws about slaves."

**[Transition to framework]**

"So we just received the Torah at Sinai. Ten Commandments. Big moment. What comes next?

Most people think: more spiritual stuff, right? More ''believe in God'' type commandments?

Nope. We get... employment law. Slavery regulations. Financial disputes.

Why? What''s the Torah doing here?"

---

### THE FRAMEWORK (8 minutes)

**[Build the parallel structure]**

"Let me show you something fascinating. At the end of last week''s parsha - Yisro - we got three prohibitions:
1. Don''t make idols
2. Don''t use metal tools on the altar
3. Don''t make steps leading up to the altar

These correspond to the three cardinal sins - the three things a Jew would give their life rather than violate:
- Idol worship
- Murder
- Adultery

*[Pause]*

Now here''s what Rav Wolbe points out: The Torah doesn''t just tell you what NOT to do. It immediately follows with three POSITIVE commandments that are the flip side of those same sins.

**The Torah is saying: Here''s the floor - don''t fall below this. But here''s the CEILING - here''s who you can become.**

Every mitzvah has layers. Every commandment applies to every person at their level. You''re not just avoiding the bad - you''re climbing toward the good."

**[Write on board or show visually]**
```
DON''T (Floor)          →    DO (Ceiling)
───────────────────────────────────────
Idol Worship           →    Eved Nirtzah (True Service)
Murder                 →    Eved Ivri (Ultimate Love)
Adultery               →    Amah Ivriah (Protecting Dignity)
```

"Let''s unpack each one."

---

### SECTION 1: EVED IVRI - From Murder to Love (15 minutes)

**[Setup the scenario]**

"Imagine this: A guy steals. Can''t pay it back. The court sells him as a servant to work off his debt. Six years of labor.

Now here''s the crazy part - the Torah tells the MASTER: You have to treat this criminal like an equal. Same bed as you? He gets the same bed. Same food? Same food. Same luxuries? Same luxuries.

*[Pause - let the absurdity land]*

Who would buy a servant just to treat them like a guest? Why would anyone sign up for this?"

**[The insight]**

"The answer is: Ahavas Yisroel - love for your fellow Jew - at its highest level.

This isn''t about the minimum. The minimum is ''don''t kill people.'' That''s the floor.

The ceiling? You treat someone who has fallen to the lowest point - a thief, a criminal - with the same dignity you''d want for yourself. Not because they deserve it. Because of who YOU are.

*[Story: Rav Yaakov and the Boy]*

Rav Yaakov Kamenetzky - one of the greatest rabbis of the 20th century - was once in a doctor''s waiting room with a young Jewish boy from a completely non-religious family. Rav Yaakov took out a ball and started playing with the kid.

His student was shocked. ''Rabbi, shouldn''t you teach him something? Or at least be learning Torah yourself?''

Rav Yaakov said: ''I saw that with this boy, it''s impossible right now to talk about Torah. He comes from a family so far removed from anything Jewish. So I just wanted that his picture of a religious Jew should remain one of a pleasant person. I played ball with him. Who knows - maybe this impression will one day have an effect.''

*[Land the connection]*

That''s the Eved Ivri principle. The opposite of murder isn''t just ''not killing.'' It''s radical connection. It''s treating every person - especially those who have fallen - with dignity they haven''t earned. Because your character isn''t determined by how you treat people who deserve it. It''s determined by how you treat people who don''t."

**[Question for the room]**

"Think about someone in your life who''s hard to respect. Maybe they''ve made bad choices. Maybe they''ve hurt you. What would it look like to treat them with dignity anyway - not because of who THEY are, but because of who YOU want to be?"

---

### SECTION 2: EVED NIRTZAH - Who Is Your Master? (12 minutes)

**[Setup]**

"So the servant works for six years. Time''s up. He''s free.

But what if he says: ''Actually, I love it here. I don''t want to leave. My master takes care of everything - food, housing, decisions. I want to stay.''

The Torah says: Fine. But we''re going to pierce your ear. And you''re stuck until Yovel - the Jubilee year, which could be decades away.

Why pierce specifically the ear?"

**[The Gemara''s answer]**

"The Talmud explains: This ear heard at Mount Sinai ''For to Me the children of Israel are servants'' - you belong to God, not to other people. And yet this person chose to acquire a human master. The ear that heard ''you''re free'' gets marked because it chose chains.

*[Pause]*

But wait - if being a servant is bad, why wasn''t he punished for the first six years?"

**[The deeper insight]**

"Here''s the key: During those first six years, he still had an option. If he got enough money, he could buy himself out. He was working, but he wasn''t OWNED.

The moment he chooses to stay? He gives up that option entirely. He says: ''I don''t want freedom. I don''t want to be responsible for myself. Someone else can make my decisions.''

That''s what gets punished. Not service - surrender. Not working for someone - giving up your autonomy entirely.

*[Modern application]*

Let me ask you: In 2026, what are the modern ''masters'' people surrender to?

- Career: ''I can''t take a day off, I can''t miss this opportunity, I have to keep climbing''
- Money: ''I know I should spend time with my family, but I need to hit this number first''
- Approval: ''I can''t do what I actually believe because people will judge me''

*[Story: The Chofetz Chaim''s Store]*

The Chofetz Chaim - the greatest rabbi of his generation - ran a little grocery store with his wife. One day a customer comes in, and the Chofetz Chaim says: ''I''ve already made enough profit for today. Come back tomorrow.''

Who turns away business? But for him, the money was a tool, not a master. The moment it started controlling him - the moment he ''needed'' more - he was no longer free.

*[Land it]*

The Nirtzah teaches us: Everyone serves something. The question is whether you''ve chosen your master consciously - or whether you''ve surrendered without realizing it.

Idol worship isn''t just about statues. It''s about giving your allegiance to anything other than your highest purpose. The elevation from ''don''t worship idols'' is ''serve only what deserves your service.''"

---

### SECTION 3: AMAH IVRIAH - Protecting Dignity (10 minutes)

**[Setup - be sensitive here]**

"The third case is the most uncomfortable to modern ears. A father is so poor he can''t feed his daughter. He sells her as a maidservant.

Before you react - listen to what the Torah actually requires:

The master must either marry her himself OR give her to his son as a wife. Either way, she must be treated with the full dignity of a regular wife. Not as a servant. As family."

**[The why]**

"The Torah is being incredibly realistic about human nature. If a young woman has no support, no family backing, no resources - what happens to her in the ancient world? She becomes vulnerable. She becomes exploited.

The Torah says: We don''t let that happen. Someone MUST take responsibility for her. And not just responsibility - dignity. Full status. Full protection.

*[The connection to Arayos]*

This is the elevation from the prohibition of adultery. The floor is: don''t exploit people sexually. The ceiling is: actively protect vulnerable people from exploitation.

*[Story: Mar Ukva and the Hot Oven]*

There was a Talmudic sage named Mar Ukva who had a beautiful custom. Every day, he would secretly slip money under a poor man''s door. Never wanted to be thanked. Never wanted the man to feel indebted.

One day, the poor man wanted to discover his benefactor. When Mar Ukva came that night, the man started opening the door. Mar Ukva and his wife ran - and ended up hiding in a hot oven that had just been used for baking.

They literally risked burning themselves rather than let this man know who was helping him.

*[The principle]*

That''s what protecting dignity looks like. It''s not about YOU feeling good for helping. It''s about THEM not feeling ashamed for receiving.

The Amah Ivriah law says: Even in the worst circumstances - poverty, desperation - every person deserves to be treated as a person. Not as an object. Not as a charity case. As someone with inherent worth."

---

### TAKEAWAY (8 minutes)

**[Bring it together]**

"So let''s step back. What did we just see?

The Torah gives us three ''don''ts'':
- Don''t worship idols
- Don''t murder
- Don''t commit adultery

And immediately follows with three ''do''s'' that show us the CEILING, not just the floor:
- Serve only what deserves your service (Nirtzah)
- Love even those who have fallen (Eved Ivri)
- Protect the dignity of the vulnerable (Amah Ivriah)

*[The Reb Wolbe insight]*

Rav Wolbe''s point is: Every mitzvah works this way. There''s a minimum - the floor. But there are levels above that, all the way to the ceiling.

And here''s what that means for you:

**Wherever you are right now, there''s a next level.**

You''re not comparing yourself to someone else''s ceiling. You''re asking: What''s MY next step? What''s the elevation that''s available to me right now?

*[Story: Reb Zusha]*

Reb Zusha was lying on his deathbed, crying. His students tried to comfort him: ''Rabbi, you were almost as wise as Moses, as kind as Abraham!''

He said: ''When I get to Heaven, they won''t ask me why I wasn''t Moses. They''ll ask me: Zusha, why weren''t you Zusha? Why didn''t you become everything YOU could have been?''

*[Final question]*

So here''s my question for you:

If mitzvos aren''t just rules but a ladder - if every commandment has a floor AND a ceiling - what''s one area of your life where you''ve been living at the floor?

Not because you''re bad. Just because you never realized there was a ceiling to reach for.

What would it look like to take one step up?"

*[Pause - let it land]*

"The Torah doesn''t just tell us what not to do. It shows us who we can become. The only question is: Are we climbing?"

